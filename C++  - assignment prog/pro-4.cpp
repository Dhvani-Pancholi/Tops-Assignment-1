// Write a program that asks for two numbers and displays their sum.

#include <iostream>
using namespace std;

int main(){
    int a,b,sum;
    cout << "Enter the first number: ";
    cin >> a;
    cout << "Enter the second number: ";
    cin >> b;
    sum = a + b;
    cout << "Sum of given numbers is: " << sum;
    return 0;

}
